package java27;

public class Bank {
	int money;
	String location;
	
	public Bank(int money, String location) {
		this.money = money;
		this.location = location;
	}
	@Override
	public String toString() {
		return "Bank [money=" + money + ", location=" + location + "]";
	}
	
	
	
	
	
	
}
